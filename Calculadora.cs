using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MiCalculadora.Clases
{
    public static class Calculadora
    {

        // PROBLEMA 1
        public static double Sumar(double valor1, double valor2)
        {
            return valor1 + valor2;
        }

        // PROBLEMA  2
        public static int Sumar(int valor1, int valor2)
        {
            return valor1 - valor2;
        }

        // PROBLEMA 3
        public static double Restar(double valor1, double valor2)
        {
            return valor1 - valor2;
        }

        public static int Restar(int valor1, int valor2)
        {
            return valor1 - valor2;
        }

        // PROBLEMA 4
        public static double Multiplicar(double valor1, double valor2)
        {
            return valor1 * valor2;
        }

        public static int Multiplicar(int valor1, int valor2)
        {
            return valor1 * valor2;
        }

        public static double Dividir(double valor1, double valor2)
        {
            return valor1 / valor2;
        }

        public static int Dividir(int valor1, int valor2)
        {
            return valor1 / valor2;
        }
    }
}
